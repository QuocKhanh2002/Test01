    public void HienThiLoaiSach(){
    ListLS = DAOLoaiSach.getListLoaiSach();
    tableModel.setRowCount(0);
    
    for(LoaiSach ls : ListLS){
        //ham for-each duyet qua cac pt ma khong can index;
        //Dữ liệu được thêm vào bằng cách sử dụng addRow
        tableModel.addRow(new Object[]{ls.getID(), ls.getMaSach(), ls.getTenSach(), decimalformat.format(ls.getDonGia()) + "đ", ls.getSoLuong(), ls.getNgayXuatBan(), ls.getLoaiSach(), ls.getNhaXuatBan()});
    }
    tableModel.fireTableDataChanged();
}

