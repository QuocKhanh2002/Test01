    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
         selectedIndex = tbLoaiSach.getSelectedRow();
        if(selectedIndex == 0){
            JOptionPane.showMessageDialog(this, "Hay Chon Sach De Xoa");
        }
        else{
            LoaiSach ls = ListLS.get(selectedIndex);
            int option = JOptionPane.showConfirmDialog(this, "Ban co muon xoa Loai Sach Nay chu !!!");
            System.out.print("option: " + option);
            if(option == 0){
                DAOLoaiSach.DeleteLoaiSach(ls);
                HienThiLoaiSach();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

